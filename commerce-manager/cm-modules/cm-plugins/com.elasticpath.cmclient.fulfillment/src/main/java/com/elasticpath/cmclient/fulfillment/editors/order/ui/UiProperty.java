package com.elasticpath.cmclient.fulfillment.editors.order.ui;


/**
 * UI properties for display data in readonly, write or masked modes.
 *
 */
public enum UiProperty {

	/** Allow to view data. */
	READ_ONLY,
	/** Allow to read/write data. */
	READ_WRITE,
	/** Mask data. No write permissions here. */
	MASK

}
